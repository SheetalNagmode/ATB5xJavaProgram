package src.basics_03;

public class Lab044 {
    public static void main(String[] args) {

        // Decrement Operator

        int b = 10;
        System.out.println(--b + b++ + ++b);
        System.out.println(b);

        //  Part A → Exp = 9, b=9 → --b
        //  Part B → Exp = 9 , b= 10 → b++
        //  Part C → Exp = 11 , b= 11 → ++b
        //  A+B+C → 9+9+11= 29 , b = 11






    }
}
